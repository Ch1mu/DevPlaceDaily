package classesEx1;

public class Songs {

	String artist;
	String link;
	
	public Songs(String artist, String link) {
		
		this.artist = artist;
		this.link = link;
	}
	public String getArtist() {
		return artist;
	}
	public void setArtist(String artist) {
		this.artist = artist;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	
}
